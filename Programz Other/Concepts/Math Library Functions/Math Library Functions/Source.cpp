#include<iostream>
#include<cmath>//always include it for using math operators
using namespace std;
int main()
{
	double x=16.4;
	cout<<ceil(x)<<endl;
	cout<<cos(x)<<endl;
	cout<<sin(x)<<endl;
	cout<<tan(x)<<endl;
	cout<<exp(x)<<endl;
	cout<<fabs(x)<<endl;//it removes negative sign
	cout<<floor(x)<<endl;
	cout<<fmod(x,2)<<endl;
	cout<<log(x)<<endl;
	cout<<log10(x)<<endl;
	cout<<pow(x,2)<<endl;
	cout<<sqrt(x)<<endl;
	return 0;
}